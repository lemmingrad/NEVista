//----------------------------------------------------------//
//--Description
//----------------------------------------------------------//
CMsgKeepAlive::CMsgKeepAlive()
: CMessage(kType, 0)
{
}


//----------------------------------------------------------//
// CMsgKeepAlive::~CMsgKeepAlive
//----------------------------------------------------------//
//--Description
//----------------------------------------------------------//
CMsgKeepAlive::~CMsgKeepAlive()
{
}


//----------------------------------------------------------//
// CMsgKeepAlive::Serialize
//----------------------------------------------------------//
//--Description
//----------------------------------------------------------//
size_t CMsgKeepAlive::Serialize(ISerializer& serializer)
{
	size_t nSize = CMessage::Serialize(serializer);
	return nSize;
}


